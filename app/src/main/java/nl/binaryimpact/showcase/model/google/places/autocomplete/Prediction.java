package nl.binaryimpact.showcase.model.google.places.autocomplete;

import java.util.List;

/**
 */
public class Prediction {

    private String description;
    private List<MatchedSubstring> matchedSubstrings;
    private String placeId;
    private String reference;
    private List<Term> terms;
    private List<String> types;

    public Prediction() {
    }

    public Prediction(String description, List<MatchedSubstring> matchedSubstrings, String placeId, String reference, List<Term> terms, List<String> types) {
        this.description = description;
        this.matchedSubstrings = matchedSubstrings;
        this.placeId = placeId;
        this.reference = reference;
        this.terms = terms;
        this.types = types;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<MatchedSubstring> getMatchedSubstrings() {
        return matchedSubstrings;
    }

    public void setMatchedSubstrings(List<MatchedSubstring> matchedSubstrings) {
        this.matchedSubstrings = matchedSubstrings;
    }

    public String getPlaceId() {
        return placeId;
    }

    public void setPlaceId(String placeId) {
        this.placeId = placeId;
    }

    public String getReference() {
        return reference;
    }

    public void setReference(String reference) {
        this.reference = reference;
    }

    public List<Term> getTerms() {
        return terms;
    }

    public void setTerms(List<Term> terms) {
        this.terms = terms;
    }

    public List<String> getTypes() {
        return types;
    }

    public void setTypes(List<String> types) {
        this.types = types;
    }
}
